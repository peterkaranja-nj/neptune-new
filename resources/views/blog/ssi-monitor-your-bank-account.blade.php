                <h2 class="text-2xl text-gray-800 font-semibold mb-4">
                    What SSI Is Really Looking For
                </h2>

                <p class="pb-6">
                    The SSA’s primary objective is to confirm that:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Your total resources remain within the permitted limits</li>
                    <li>All income and financial support are properly reported</li>
                </ul>

                <p class="pb-6 mt-6">
                    In practice, the most common issues arise from:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Bank balances temporarily exceeding the limit</li>
                    <li>Unexplained deposits</li>
                    <li>Informal financial support from family or third parties</li>
                </ul>

