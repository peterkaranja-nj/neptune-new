                    <li>Company registration</li>
                    <li>Preparing compliance documents</li>
                    <li>Showing proof of capital</li>
                    <li>Appointing directors and compliance officers</li>
                </ul>

                <p class="pb-6 mt-6">
                    Tip: Some startups begin with a white-label solution under an existing licensed broker, then apply for their own license later.
