                <h2 class="text-2xl text-gray-800 font-semibold mb-4">
                    Understand How a Forex Brokerage Works
                </h2>
                
                <p class="pb-6">
                    Before starting, it is important to understand the basics.
                </p>

                <p class="pb-6">
                    A forex brokerage firm acts as a middleman between traders and the market. You provide a platform where clients can buy and sell currencies. Your income mainly comes from:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Spreads (difference between buy and sell price)</li>
                    <li>Commissions on trades</li>
                    <li>Swap fees (overnight charges)</li>
                </ul>

                <p class="pb-6 mt-6">
                    There are two main models:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Market Maker (B-Book): You take the opposite side of client trades</li>
                    <li>STP/ECN (A-Book): Trades are sent directly to liquidity providers</li>
                </ul>

                <p class="pb-6 mt-6">
                    Many modern brokers use a hybrid model to balance risk and profit.
                </p>

