                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li><strong>Full banking license:</strong> The fullest kind of authorisation is this. It enables banks to accept deposits, make loans, and provide full retail and corporate banking services with tight financial services regulation.</li>
                    <li><strong>Offshore banking license:</strong> This license is popular with international businesses since they are allowed to conduct banking operations primarily for non-residents. The holder of an offshore banking opening frequently has international clients while adhering to international compliance requirements.</li>
                    <li><strong>Restricted banking license:</strong> A restricted variation that enables banks to perform with specific limitations, e.g., fixed deposits or restricted services. New or trial institutions use this most of the time.</li>
                    <li><strong>Digital or virtual bank license:</strong> This international banking licence is created to accommodate banks operating online but fell within the stipulated rules of the regulated banking system.</li>
                </ul>

