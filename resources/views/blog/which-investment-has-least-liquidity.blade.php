                <h2 class="text-2xl text-gray-800 font-semibold mb-4">
                    Why Low Liquidity Can Be Risky
                </h2>

                <p class="pb-6">
                    Low-liquidity Investments are not inherently bad—but they require careful planning.
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Limited access to cash during emergencies</li>
                    <li>Forced sales at lower prices</li>
                    <li>Uncertain exit timelines</li>
                    <li>Dependence on market conditions or third parties</li>
                </ul>

