                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Meet short-term obligations</li>
                    <li>Take advantage of opportunities</li>
                    <li>Avoid selling long-term investments at the wrong time</li>
                </ul>

                
                <h2 class="text-2xl text-gray-800 font-semibold mb-4 mt-6">
                    Practical Insight: A Common Mistake
