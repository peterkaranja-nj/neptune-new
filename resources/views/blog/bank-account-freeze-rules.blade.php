                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li><strong>Keep Your Information Updated:</strong> Always update your bank if your address, company structure, or business activity changes.</li>
                    <li><strong>Be Transparent About Your Business:</strong> Banks prefer clients who clearly explain their business model and source of funds.</li>
                    <li><strong>Avoid Unusual Transactions:</strong> Large or sudden transfers can trigger alerts. Inform your bank before making significant transactions.</li>
                    <li><strong>Maintain Proper Documentation:</strong> Businesses should keep clear records of invoices, contracts, and financial transactions.</li>
