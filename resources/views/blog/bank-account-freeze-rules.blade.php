                </h2>

                <p class="pb-6">
                    The length of a freeze depends on the reason. Typical timeframes include:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>24–72 hours for basic security checks</li>
                    <li>Several days or weeks for compliance investigations</li>
                    <li>Longer periods if legal authorities are involved</li>
                </ul>

                <p class="pb-6 mt-6">
                    The bank usually removes the freeze once the issue has been clarified.
                </p>

