                <h2 class="text-2xl text-gray-800 font-semibold mb-4">
                    What Is Original Jurisdiction?
                </h2>
                
                 <img src="/images/images/what-is-original-jurisdiction.jpg" alt="What Is Original Jurisdiction" class="w-full object-cover lg:rounded pb-4" style="height: 28em;" />


                <p class="pb-6">
                    Original jurisdiction refers to the authority of a court to hear a case for the first time.
                </p>

                <p class="pb-6">
                    This is the stage where:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Evidence is presented</li>
                    <li>Witnesses may be called</li>
                    <li>Facts are established</li>
                    <li>A decision (judgment) is made</li>
                </ul>

                <p class="pb-6 mt-6">
                    In simple terms, this is where a case begins.
                </p>

                <h3 class="text-xl text-gray-800 font-semibold mb-2">
                    Typical Examples
                </h3>

                <p class="pb-6">
                    Courts exercising original jurisdiction may handle:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Civil disputes (e.g. contracts, property matters)</li>
                    <li>Criminal cases</li>
                    <li>Constitutional matters (in certain higher courts)</li>
                </ul>

                <p class="pb-6 mt-6">
                    In some jurisdictions, higher courts may also have original jurisdiction in special or high-value matters, depending on the legal framework.
                </p>
