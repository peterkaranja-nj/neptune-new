                <h2 class="text-2xl text-gray-800 font-semibold mb-4">
                    What Is Appellate Jurisdiction?
                </h2>

                <p class="pb-6">
                    Appellate jurisdiction refers to the authority of a court to review and possibly revise a decision made by a lower court.
                </p>

                <p class="pb-6">
                    This does not involve starting a case from scratch. Instead, the court examines:
                </p>

                <ul class="list-disc px-4 ml-4 text-sm mb-6">
                    <li>Whether the law was applied correctly</li>
                    <li>Whether proper procedures were followed</li>
                    <li>Whether the decision was reasonable based on the evidence</li>
                </ul>

                <h3 class="text-xl text-gray-800 font-semibold mb-2 mt-6">
                    Key Point
                </h3>

                <p class="pb-6">
                    Appellate courts generally do not rehear all the evidence or call new witnesses. Their role is to assess the correctness of the earlier decision.
                </p>

